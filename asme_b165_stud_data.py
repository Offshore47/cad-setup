def get_b165_stud_info(nps, pressure_class, facing_type='RF'):
    """
    Get ASME B16.5 stud dimensions for a specific NPS, pressure class, and facing type.
    
    Args:
        nps (str): Nominal pipe size (e.g., '1/2', '1', '1-1/2')
        pressure_class (int): Pressure class (150, 300, 600, 900, 1500, 2500)
        facing_type (str): Flange facing type - 'RF' (Raised Face) or 'RTJ' (Ring Type Joint)
    
    Returns:
        dict: Stud dimensions including ring_number for RTJ, or None if not found
    """
    # RF (Raised Face) stud data
    rf_class_map = {
        150: B165_CLASS_150_STUDS,
        300: B165_CLASS_300_STUDS,
        400: B165_CLASS_400_STUDS,
        600: B165_CLASS_600_STUDS,
        900: B165_CLASS_900_STUDS,
        1500: B165_CLASS_1500_STUDS,
        2500: B165_CLASS_2500_STUDS
    }
    
    # RTJ (Ring Type Joint) stud data
    rtj_class_map = {
        400: B165_CLASS_400_RTJ_STUDS,
        600: B165_CLASS_600_RTJ_STUDS,
        900: B165_CLASS_900_RTJ_STUDS,
        1500: B165_CLASS_1500_RTJ_STUDS,
        2500: B165_CLASS_2500_RTJ_STUDS
    }
    
    # Select the appropriate map based on facing type
    if facing_type.upper() == 'RTJ':
        class_map = rtj_class_map
    else:
        class_map = rf_class_map
    
    stud_dict = class_map.get(pressure_class)
    if stud_dict:
        return stud_dict.get(nps)
    return None


def list_b165_sizes(pressure_class):
    """
    List all available NPS sizes for a given ASME B16.5 pressure class.
    
    Args:
        pressure_class (int): Pressure class (150, 300, 400, 600, 900, 1500, 2500)
    
    Returns:
        list: Available NPS sizes sorted
    """
    class_map = {
        150: B165_CLASS_150_STUDS,
        300: B165_CLASS_300_STUDS,
        600: B165_CLASS_600_STUDS,
        900: B165_CLASS_900_STUDS,
        1500: B165_CLASS_1500_STUDS,
        2500: B165_CLASS_2500_STUDS
    }
    
    stud_dict = class_map.get(pressure_class)
    if stud_dict:
        # Custom sort for fractional NPS
        def nps_to_float(nps_str):
            if '-' in nps_str and '/' in nps_str:
                # Handle '1-1/2' format
                parts = nps_str.split('-')
                whole = float(parts[0])
                frac_parts = parts[1].split('/')
                frac = float(frac_parts[0]) / float(frac_parts[1])
                return whole + frac
            elif '/' in nps_str:
                # Handle '1/2' format
                parts = nps_str.split('/')
                return float(parts[0]) / float(parts[1])
            else:
                # Handle '1', '2', etc.
                return float(nps_str)
        
        return sorted(stud_dict.keys(), key=nps_to_float)
    return []



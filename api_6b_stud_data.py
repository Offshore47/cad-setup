def get_api_6b_stud_info(size, rating_psi):
    """
    Get API 6B stud dimensions for a specific flange size and pressure rating.
    
    Args:
        size (str): Flange size (e.g., '1-13/16', '2-1/16', '13-5/8')
        rating_psi (int): Pressure rating (2000, 3000, 5000, 10000, 15000, or 20000)
    
    Returns:
        dict: Stud dimensions or None if not found
    """
    rating_map = {
        2000: API_6B_2000_STUDS,
        3000: API_6B_3000_STUDS,
        5000: API_6B_5000_STUDS,
        10000: API_6B_10000_STUDS,
        15000: API_6B_15000_STUDS,
        20000: API_6B_20000_STUDS,
    }
    
    stud_dict = rating_map.get(rating_psi)
    if stud_dict:
        return stud_dict.get(size)
    return None


def list_api_6b_sizes(rating_psi):
    """
    List all available API 6B flange sizes for a given pressure rating.
    
    Args:
        rating_psi (int): Pressure rating (2000, 3000, 5000, 10000, 15000, or 20000)
    
    Returns:
        list: Available flange sizes
    """
    rating_map = {
        2000: API_6B_2000_STUDS,
        3000: API_6B_3000_STUDS,
        5000: API_6B_5000_STUDS,
        10000: API_6B_10000_STUDS,
        15000: API_6B_15000_STUDS,
        20000: API_6B_20000_STUDS,
    }
    
    stud_dict = rating_map.get(rating_psi)
    if stud_dict:
        return sorted(stud_dict.keys())
    return []


